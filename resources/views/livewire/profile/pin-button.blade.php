<?php

use Illuminate\Support\Facades\Auth;
use Livewire\Volt\Component;

new class extends Component
{
    public $user;
    public $isPinned;

    public function mount($user)
    {
        $this->user = $user;
        
        // Check if this user is pinned in the current user's following list
        $followedUser = Auth::user()->following()->where('followed_id', $this->user)->first();
        $this->isPinned = $followedUser ? $followedUser->pivot->pinned : false;
    }

    public function togglePin()
    {
        if ($this->isPinned) {
            // Unpin the user
            Auth::user()->following()->updateExistingPivot($this->user, ['pinned' => false]);
        } else {
            // Pin the user
            Auth::user()->following()->updateExistingPivot($this->user, ['pinned' => true]);
        }

        // Toggle the pinned status
        $this->isPinned = !$this->isPinned;

        // Emit an event to update the button
        $this->dispatch('pinnedToggled', $this->isPinned);
    }
}
